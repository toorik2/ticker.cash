//! `ticker-ops bake` — produce a self-extracting bash installer.
//!
//! The installer:
//!   1. Writes the manifest.json + per-role key to `~/.ticker/` (mode 0600 for key).
//!   2. Downloads the ticker-node binary from `--binary-url`.
//!   3. Verifies the binary against `--binary-sha256`.
//!   4. Installs to `~/.local/bin/ticker-node` (or `/usr/local/bin/` with `--system`).
//!   5. Drops a systemd user unit + enables it.
//!
//! The installer is a single `.sh` file: shell prelude + base64-encoded payload.

use std::fs;
use std::os::unix::fs::PermissionsExt;

use ticker_core::identity::seed::{derive_wallet, load_seed};

/// Base64-encode without dependencies (~30 LOC, standard alphabet).
fn b64_encode(data: &[u8]) -> String {
    const A: &[u8; 64] = b"ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/";
    let mut out = String::with_capacity((data.len() + 2) / 3 * 4);
    let mut i = 0;
    while i + 3 <= data.len() {
        let b = (data[i] as u32) << 16 | (data[i + 1] as u32) << 8 | data[i + 2] as u32;
        out.push(A[((b >> 18) & 0x3f) as usize] as char);
        out.push(A[((b >> 12) & 0x3f) as usize] as char);
        out.push(A[((b >> 6) & 0x3f) as usize] as char);
        out.push(A[(b & 0x3f) as usize] as char);
        i += 3;
    }
    let rem = data.len() - i;
    if rem == 1 {
        let b = (data[i] as u32) << 16;
        out.push(A[((b >> 18) & 0x3f) as usize] as char);
        out.push(A[((b >> 12) & 0x3f) as usize] as char);
        out.push_str("==");
    } else if rem == 2 {
        let b = (data[i] as u32) << 16 | (data[i + 1] as u32) << 8;
        out.push(A[((b >> 18) & 0x3f) as usize] as char);
        out.push(A[((b >> 12) & 0x3f) as usize] as char);
        out.push(A[((b >> 6) & 0x3f) as usize] as char);
        out.push('=');
    }
    out
}

pub fn bake(
    seed_path: &str,
    manifest_path: &str,
    role: &str,
    slot: u8,
    binary_url: &str,
    binary_sha256: &str,
    output_path: &str,
) -> Result<(), Box<dyn std::error::Error>> {
    if role != "notary" && role != "publisher" {
        return Err("--role must be 'notary' or 'publisher'".into());
    }

    let seed = load_seed(seed_path)?;
    let label = if role == "notary" {
        format!("notary-{slot}")
    } else {
        format!("publisher-{slot}")
    };
    let wallet = derive_wallet(&seed, &label)?;
    let key_hex = hex::encode(wallet.private_key);
    let manifest_bytes = fs::read(manifest_path)?;

    let manifest_b64 = b64_encode(&manifest_bytes);

    let script = format!(r#"#!/usr/bin/env bash
# ticker-node installer for {role} slot {slot}
# Generated by ticker-ops bake. Do not edit by hand.

set -euo pipefail

TICKER_HOME="${{TICKER_HOME:-$HOME/.ticker}}"
BINARY_URL="{binary_url}"
BINARY_SHA256="{binary_sha256}"
INSTALL_BIN="${{INSTALL_BIN:-$HOME/.local/bin/ticker-node}}"
UNIT_NAME="ticker-node-{role}@{slot}.service"

echo "ticker-node installer — {role} slot {slot}"
echo "  TICKER_HOME = $TICKER_HOME"

mkdir -p "$TICKER_HOME"
mkdir -p "$(dirname "$INSTALL_BIN")"
mkdir -p "$HOME/.config/systemd/user"

# Drop the manifest.
echo "  installing manifest…"
base64 -d <<'MANIFEST_B64' > "$TICKER_HOME/manifest.json"
{manifest_b64}
MANIFEST_B64

# Drop the operator key (mode 0600).
echo "  installing {role}.key…"
umask 077
printf '%s\n' '{key_hex}' > "$TICKER_HOME/{role}.key"
umask 022

# Download + verify the binary.
echo "  downloading ticker-node binary…"
TMP_BIN=$(mktemp)
trap "rm -f $TMP_BIN" EXIT
curl --proto =https --tlsv1.2 -fsSL "$BINARY_URL" -o "$TMP_BIN"
ACTUAL_SHA=$(sha256sum "$TMP_BIN" | awk '{{print $1}}')
if [ "$ACTUAL_SHA" != "$BINARY_SHA256" ]; then
  echo "  ERROR: binary sha256 mismatch (got $ACTUAL_SHA, expected $BINARY_SHA256)" >&2
  exit 1
fi
install -m 0755 "$TMP_BIN" "$INSTALL_BIN"

# Install systemd user unit.
echo "  writing systemd unit $UNIT_NAME…"
cat > "$HOME/.config/systemd/user/$UNIT_NAME" <<UNIT
[Unit]
Description=ticker.cash {role} slot {slot}
After=network-online.target
Wants=network-online.target

[Service]
Type=simple
WorkingDirectory=$HOME
ExecStart=$INSTALL_BIN --{role}
Restart=on-failure
RestartSec=15s
StandardOutput=journal
StandardError=journal

[Install]
WantedBy=default.target
UNIT

systemctl --user daemon-reload
systemctl --user enable --now "$UNIT_NAME"

echo
echo "installed. logs: journalctl --user -fu $UNIT_NAME"
"#);

    fs::write(output_path, &script)?;
    let mut perms = fs::metadata(output_path)?.permissions();
    perms.set_mode(0o700);
    fs::set_permissions(output_path, perms)?;
    println!("baked installer → {output_path}  ({} bytes)", script.len());
    Ok(())
}
